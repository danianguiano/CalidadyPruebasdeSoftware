# Prepare files for a new development sprint, increasing version number.
#
# Usage:
#   $0 NEXT_RELEASE_NUMBER
#   $0 --help
#
# Options:
#   -h, --help		show this help message and exit
#

OPTS='h(help)'
PROG=$(basename $0)

# Command line options
NEXT_RELEASE_NUMBER=

# Command line processing
OPTERR=
OPTSTR=$(echo :-:$OPTS | sed 's/([a-zA-Z0-9]*)//g')
OPTHLP=$(sed -n '20,/^$/ { s/$0/'$PROG'/; s/^#[ ]\?//p }' $0)
while getopts $OPTSTR OPT; do while [ -z "$OPTERR" ]; do
case $OPT in
'h')	OPTERR="$OPTHLP";;
'?')	OPTERR="Unknown option -$OPTARG";;
':')	OPTERR="Missing value for option -$OPTARG";;
'-')	OPTLONG="${OPTARG%=*}";
	OPT=$(expr $OPTS : ".*\(.\)($OPTLONG):.*" '|' '?');
	if [ "$OPT" = '?' ]; then
		OPT=$(expr $OPTS : ".*\(.\)($OPTLONG).*" '|' '?')
		OPTARG=-$OPTLONG
	else
		OPTARG=$(echo =$OPTARG | cut -d= -f3)
		[ -z "$OPTARG" ] && { OPTARG=-$OPTLONG; OPT=':'; }
	fi;
	continue;;
esac; break; done; done
shift $(expr $OPTIND - 1)
[ -n "${NEXT_RELEASE_NUMBER:=$1}" ] && shift
[ -z "$OPTERR" -a -z "$NEXT_RELEASE_NUMBER" ] && OPTERR="Missing release number"
[ -z "$OPTERR" -a -n "$*" ] && OPTERR="Too many arguments"
[ -n "$OPTERR" ] && {
	PREAMBLE=$(printf "$OPTHLP" | sed -n '0,/^Usage:/ p' | head -n -1)
	USAGE="Usage:\n"$(printf "$OPTHLP" | sed '0,/^Usage:/ d')"\n\n"
	TAB=4; LEN=$(echo "$USAGE" | awk -F'\t' '/ .+\t/ {print $1}' | wc -L)
	TABSTOPS=$TAB,$(((LEN/TAB+2)*TAB)); WIDTH=${COLUMNS:-$(tput cols)}
	[ "$OPTERR" != "$OPTHLP" ] && PREAMBLE="ERROR: $OPTERR"
	printf "$PREAMBLE\n\n" | fmt -$WIDTH 1>&2
	printf "$USAGE" | tr -s '\t' | expand -t$TABSTOPS | fmt -$WIDTH -s 1>&2
	exit 1
}

# Common properties
PROGDIR=$(readlink -f $(dirname $0))
BASEDIR=$(readlink -f $PROGDIR/../..)

# Component properties
DOCKERFILE=$BASEDIR/docker/Dockerfile

# Function to update develop branch with latest changes
update_develop_branch() {
	git fetch origin
	git checkout --quiet develop
	git pull origin develop
	git pull origin master
}

# Function to change release number and reset dockerfiles
bump_next_release() {
	sed -i "s/GIT_REV .*/GIT_REV develop/" $DOCKERFILE
	git add $DOCKERFILE
	cd $BASEDIR
	bumpversion --allow-dirty --new-version=$NEXT_RELEASE_NUMBER patch
	git push origin develop
}

# Main
update_develop_branch \
&& bump_next_release
